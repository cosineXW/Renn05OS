// ────────────────────────────────────────────────────────────────
//  算法 A：发帖频率衰减（Traffic Factor K）
// ────────────────────────────────────────────────────────────────
//  模型：指数恢复 K = 1 - e^(-t/12)
//  含义：距上次发帖越久，本次帖子获得的"流量系数"K 越高
//        半衰期 ≈ 8.3h → 24h 后 K ≈ 0.86
//        刷帖惩罚：间隔 < 1h → K ≈ 0.08（几乎无流量）
//  爆款：5% 概率触发"玄学爆款"，K 直接设为 3~5（绕过衰减）
// ────────────────────────────────────────────────────────────────
function calcTrafficFactor(diffHours) {
    let K = 1 - Math.exp(-diffHours / 12);
    if (Math.random() < 0.05) K = 3 + Math.random() * 2;
    return K;
}

// ────────────────────────────────────────────────────────────────
//  算法 B：连续活跃奖励（Streak Bonus）
// ────────────────────────────────────────────────────────────────
//  模型：multiplier = min(1 + days × 0.1, 1.5)
//  含义：连续每天发帖可获得加成，5 天封顶 1.5x
//        用于放大帖子的赞数/评论数
//  断更：streak 归 1 重新开始
// ────────────────────────────────────────────────────────────────
function calcStreakMultiplier(streakDays) {
    return Math.min(1 + streakDays * 0.1, 1.5);
}

// ────────────────────────────────────────────────────────────────
//  Toast 通知（UI 工具函数）
// ────────────────────────────────────────────────────────────────
//  延迟 100ms 确保 DOM 渲染完成后再显示
// ────────────────────────────────────────────────────────────────
function showToast(message, duration = 3000) {
    setTimeout(() => {
        const toast = document.getElementById("fan-toast");
        if (!toast) return;
        toast.innerText = message;
        toast.classList.remove("show");
        void toast.offsetWidth;
        toast.classList.add("show");
        setTimeout(() => toast.classList.remove("show"), duration);
    }, 100);
}
